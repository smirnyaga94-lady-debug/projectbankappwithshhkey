# =====================================================================
# 1. ТЕСТЫ УСПЕШНОГО ПАРСИНГА (Валидные данные)
# =====================================================================
@pytest.mark.parametrize(
    "input_str, expected_output",
    [
        # Стандартный формат с миллисекундами
        ("2026-07-13T14:36:00.123456", "13.07.2026"),
        # Формат без миллисекунд (точка отсутствует)
        ("2026-07-13T14:36:00", "13.07.2026"),
        # Граничное время: начало суток
        ("2026-01-01T00:00:00.000", "01.01.2026"),
        # Граничное время: конец суток
        ("2026-12-31T23:59:59.999", "31.12.2026"),
        # Проверка високосного года
        ("2028-02-29T12:00:00.000", "29.02.2028"),
    ],
)
def test_get_date_success(input_str, expected_output):
    """Проверка корректного преобразования валидных строк в формат ДД.ММ.ГГГГ."""
    assert get_date(input_str) == expected_output


# =====================================================================
# 2. ТЕСТЫ НЕВЕРНОГО ФОРМАТА И ГРАНИЧНЫХ ОШИБОК
# =====================================================================
@pytest.mark.parametrize(
    "invalid_str",
    [
        "13.07.2026T14:36:00",  # Неверный порядок (ДД.ММ.ГГГГ вместо ГГГГ-ММ-ДД)
        "2026-07-13 14:36:00",  # Вместо 'T' используется пробел
        "2026/07/13T14:36:00",  # Неверный разделитель даты (слэш)
        "2026-02-29T12:00:00",  # 29 февраля в невисокосный год
        "2026-13-01T12:00:00",  # Несуществующий месяц (13)
        "2026-07-32T12:00:00",  # Несуществующий день (32)
        "Двадцать пятое июля",  # Обычный текст
    ],
)
def test_get_date_invalid_format(invalid_str):
    """Проверка возврата сообщения об ошибке при невалидных данных."""
    assert get_date(invalid_str) == "Неверный формат данных"


# =====================================================================
# 3. ТЕСТЫ ОТСУТСТВИЯ ДАТЫ И НЕКОРРЕКТНЫХ ТИПОВ ВХОДНЫХ ДАННЫХ
# =====================================================================
@pytest.mark.parametrize(
    "missing_input",
    [
        "",  # Пустая строка
        "   ",  # Строка из пробелов
        "Встреча в парке",  # Текст, где даты вообще нет
        None,  # Передача None (проверка устойчивости к типам данных)
        123456789,  # Передача числа вместо строки
    ],
)
def test_get_date_missing_or_wrong_type(missing_input):
    """Проверка обработки пустых строк и непредвиденных типов данных."""
    assert get_date(missing_input) == "Неверный формат данных"
